function showChangeBtn () {
document.getElementById("nameTrigger").style.display="none"
document.getElementById("nameInput").style.display="block"
document.getElementById("nameSubmit").style.display="block"
}

function hideChangeBtn () {
document.getElementById("nameInput").style.display="none"
document.getElementById("nameSubmit").style.display="none"
document.getElementById("nameTrigger").style.display="block"
}